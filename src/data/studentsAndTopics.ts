export function sanitizeDocId(name: string): string {
  if (!name) return "";
  return name.trim().toLowerCase().replace(/[^a-z0-9]/g, "_");
}

export function getLocalTodayString(): string {
  const d = new Date();
  const year = d.getFullYear();
  const month = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

export function getDateConstraints() {
  const todayStr = getLocalTodayString();
  const todayDate = new Date();

  const minSchDate = new Date(todayDate);
  minSchDate.setMonth(minSchDate.getMonth() - 2);
  const minYear = minSchDate.getFullYear();
  const minMonth = String(minSchDate.getMonth() + 1).padStart(2, '0');
  const minDay = String(minSchDate.getDate()).padStart(2, '0');
  const minScheduleDate = `${minYear}-${minMonth}-${minDay}`;

  const maxSchDate = new Date(todayDate);
  maxSchDate.setMonth(maxSchDate.getMonth() + 2);
  const maxYear = maxSchDate.getFullYear();
  const maxMonth = String(maxSchDate.getMonth() + 1).padStart(2, '0');
  const maxDay = String(maxSchDate.getDate()).padStart(2, '0');
  const maxScheduleDate = `${maxYear}-${maxMonth}-${maxDay}`;

  return {
    maxCoveredDate: maxScheduleDate,
    minScheduleDate,
    maxScheduleDate
  };
}
